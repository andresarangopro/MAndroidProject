public class InventoryBean {
    List<Inventory> data;

    public class Inventory {
        private String barcode;                     //00110L",
        private String productCode;                 //001",
        private String roductName;                  //定货号测试款",
        private int qty;                            //10,
        private String skuName;                     //定货号测试款.10.##大码##",
        private String warehouseCode;               //SH500",

        public String getBarcode() {
            return barcode;
        }

        public void setBarcode(String barcode) {
            this.barcode = barcode;
        }

        public String getProductCode() {
            return productCode;
        }

        public void setProductCode(String productCode) {
            this.productCode = productCode;
        }

        public String getRoductName() {
            return roductName;
        }

        public void setRoductName(String roductName) {
            this.roductName = roductName;
        }

        public int getQty() {
            return qty;
        }

        public void setQty(int qty) {
            this.qty = qty;
        }

        public String getSkuName() {
            return skuName;
        }

        public void setSkuName(String skuName) {
            this.skuName = skuName;
        }

        public String getWarehouseCode() {
            return warehouseCode;
        }

        public void setWarehouseCode(String warehouseCode) {
            this.warehouseCode = warehouseCode;
        }
    }

    public List<Inventory> getData() {
        return data;
    }

    public void setData(List<Inventory> data) {
        this.data = data;
    }
}
